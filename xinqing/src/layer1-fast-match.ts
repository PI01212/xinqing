/**
 * 第一层快速匹配函数
 * @param message 用户原始消息
 * @returns 匹配结果或null（未匹配时返回null，交给第二层处理）
 */
export function fastMatch(message: string): FastMatchResult | null {
  const text = message.trim();

  // 长度检查：超过15字通常不是简单对话，交给AI处理
  if (text.length > 15) {
    return null;
  }

  // 遍历所有规则进行匹配
  for (const rule of FAST_PATTERNS) {
    if (rule.patterns.some(pattern => pattern.test(text))) {
      return {
        matched: true,
        type: rule.type,
        response: rule.response,
        layer: 1  // 标记为第一层处理
      };
    }
  }

  return null;  // 未匹配，交给第二层AI处理
}

